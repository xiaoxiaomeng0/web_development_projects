@app.route('/register', methods=["GET", "POST"])
def register():
    register_form = RegisterForm()
    if register_form.validate_on_submit():
        username = register_form.username.data
        email = register_form.email.data
        password = generate_password_hash(register_form.password.data, salt_length=8)
        user = User.query.filter_by(email=email).first()
        if user:
            flash("Username Taken, please try another name!")
            return redirect(url_for("login"))
        new_user = User(username=username, email=email, password=password)
        db.session.add(new_user)
        db.session.commit()
        login_user(new_user)
        return redirect(url_for("get_all_posts"))
    return render_template("register.html", form=register_form)


@app.route('/login', methods=["GET", "POST"])
def login():
    login_form = LoginForm()
    if login_form.validate_on_submit():
        email = login_form.email.data
        password = login_form.password.data
        user = User.query.filter_by(email=email).first()
        if user:
            if check_password_hash(user.password, password):
                login_user(user)
                # next = request.args.get("next")
                # if not is_safe_url(next):
                #     return abort(400)
                return redirect(url_for("get_all_posts"))
            flash("Username and password does not match!")
            return redirect(url_for("login"))
        flash("Username does not exist, please register!")
        return redirect(url_for("register"))
    return render_template("login.html", form=login_form)


